from pisi.actionsapi import autotools
from pisi.actionsapi import pisitools

def setup():
    autotools.configure()

def build():
    autotools.make()

def install():
    pisitools.dodir("/usr/share")

    autotools.install()

    pisitools.dodoc("README.txt", "doc/*.txt")
    pisitools.dohtml("doc/html/")
